const tabContent = {
  students: ["studentsTitle", "studentsBody"],
  events: ["eventsTitle", "eventsBody"],
  industry: ["industryTitle", "industryBody"]
};

let currentLang = "en";

const applyLanguage = (lang) => {
  currentLang = lang;
  document.documentElement.lang = lang === "te" ? "te" : "en";
  document.body.classList.toggle("telugu", lang === "te");

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  updateTabPanel(document.querySelector(".tab.active")?.dataset.tab || "students");
};

const updateTabPanel = (tabName) => {
  const panel = document.querySelector("#tab-panel");
  const [titleKey, bodyKey] = tabContent[tabName];
  panel.innerHTML = `
    <h3>${translations[currentLang][titleKey]}</h3>
    <p>${translations[currentLang][bodyKey]}</p>
  `;
};

document.querySelectorAll(".lang-btn").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.querySelectorAll(".tab").forEach((button) => {
  button.addEventListener("click", () => {
    document.querySelectorAll(".tab").forEach((tab) => {
      tab.classList.remove("active");
      tab.setAttribute("aria-selected", "false");
    });
    button.classList.add("active");
    button.setAttribute("aria-selected", "true");
    updateTabPanel(button.dataset.tab);
  });
});

const menuToggle = document.querySelector(".menu-toggle");
const navLinks = document.querySelector(".nav-links");

menuToggle.addEventListener("click", () => {
  const isOpen = navLinks.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

navLinks.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    navLinks.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

document.querySelector("#year").textContent = new Date().getFullYear();
